                                        <p>Bollinger Band merupakan salah satu Indikator yang digemari banyak kalangan trader. BB sudah masuk pada daftar indikator bawaan Meta Trader 4 yang dikelompokkan pada keluarga Trend. Band sendiri berasal dari Bahasa Inggris yang artinya “Pita”. Pita yang dimaksudkan karena visual dari BB adalah Garis Pita yang mengiringi pergerakan market. Sedangkan Bollinger adalah nama penemunya.
Bollinger Band diciptakan oleh John A. Bollinger. Beliau berasal dari Montpelier, Vermont, Amerika. Lahir pada 27 Mei 1950. Bekerja sebagai jurnalis Amerika, analis keuangan dan kontributor di bidang analisis tekninal serta pengembangan Bollinger Band. <br>

Indikator Bollinger Band berasal dari pengalaman  pribadi yang dituangkan pada Komputer Mikro di tahun 1977. Pada saat itu, John A. Bollinger terlibat dalam bisns komputer berbasi analisis teknis. Teknologi Komputer membuatnya dapat mengembangkan Power Group yang di dalamnya bertugas sebagai pencatat perkembangan trend di sektor industri, sehingga selanjutnya Bollinger Band terus berkembang. John A. Bollinger banyak meraih penghargaan. Salah satunya pada tahun 2005 mendapatkan penghargaan sebagai Pasar Teknisi Asosiasi dan kontribusi di bidang teknis analisis.
Konsep Dasar Indikator Bollinger Band <br>

Pada dasarnya, BB digunakan pada pendeteksian trend yang disebut volatility yaitu tingkat kecepatan dalam perubahan trend. Pada setting defaultnya, Indikator BB terdiri dari: <br>
1. Upper Band : Garis batas atas BB yang terdiri dari Simple Moving Average + (Z x Deviasi) <br>
2. Middle Band : Garis tengah BB yang terbuat dari Simple Moving Average <br>
3. Lower Band : Garis batas bawah BB yang terdiri dari Simple Moving Average - (Z x Deviasi)
Z adalah 0,6174 x in (periode) + 0,1046
Ketiga garis tersebut dijadikan satu pada setting default Periode 20, Shift 0 dan Deviation 2. <br>

Karakteristik Bollinger Band <br>
BB merupakan salah satu Indikator Lagging (pembuatannya berdasarkan market yang sudah terjadi). Namun, BB tetaplah memiliki potensi dalam menganalisa market. Setiap trader memiliki penerapan BB dengan rule masing-masing. Seperti yang sudah berkembang pada tahun 2019 ini terdapat Teknikal yang cukup terkenal dengan penggabungan BB dan Moving Average seperti BBMA Oma Ally dan BBMA Kang Gun. Setiap setup memiliki perhitungan dan karakteristik masing-masing. Namun, Bollinger Band tetaplah memiliki karakteristik dasar.
Bollinger Band bergerak mengiringi market berdasarkan pada Middle BB sebagai acuan dasar. Kemudian Upper BB atau Top BB sebagai batas resistance yang dinamis. Sebaliknya Lower BB sebagai batas  support yang dinamis. Pembacaan trend market berdasarkan posisi market terhadap Middle BB. Jika market berada di tengah antara Middle BB dan Top BB dapat diasumsikan sebagai uptrend. Jika market berada di tengah antara Middle BB dan Lower BB dapat diasumsikan sebagai downtrend. <br>

Kelebihan dari Indikator Bollinger Band <br>
1. Mendeteksi berakhirnya suatu trend <br>
2. Mengetahuhi terjadinya sideway <br>
3. Mendeteksi overbought <br>
4. Mengetahui oversold <br>
5. Dapat digunakan untuk melihat pola: double top, double bottom, dan pergantian momentum <br>

Kekurangan dari Indikator Bollinger Band <br>
1. Telat atau lambat <br>
2. Harga dapat bergerak melampaui garis band <br>


