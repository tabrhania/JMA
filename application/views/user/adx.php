                                        <p>Pengertian Average Directional Movement Index (ADX)
ADX merupakan salah satu Indikator bawaan yang di sediakan pada Platform Trading MT4. ADX termasuk salah satu tools analisa teknikal yang dikembangkan oleh J. Welles Wilder yang dituangkan dalam bukunya yang berjudul “New Concepts in Techinal Trading System” pada tahun 1978. ADX merupakan indikator pada keluarga besar trend.

Pembacaan ADX menggunakan kekuatan trend pada harga sebuah instrumen finansial, hal ini menyebabkan ADX termasuk indikator “lagging” atau terlambat. Sinyal ADX muncul setelah trend terbentuk pada harga dan  tergambar pada chart. J. Welles Wilder awalnya mengembangkan ADX sebagai indikator harga komoditas, namun kemudian berkembang dan dapat digunakan pada analisa teknikal saham dan forex juga. ADX disebut juga sebagai Directional Movement Index (DMI) yang merupakan indikator dengan kombinasi dari tiga garis, yaitu Average Directional Index, +DI dan -DI.
ADX membaca kekuatan pada suatu trend tanpa mendefinisikan trend naik atau turun. Namun, +DI dan -DI yang melengkapi ADX menampilkan pergerakan arah harga. Perhitungan ADX, +DI, dan -DI secara manual sangatlah rumit. Hal ini dikarenakan rumusan logika matematika menggunakan teori “if-else” dan penghitungan Average True Range. Namun, kita dapat dengan mudah menggunakannya karena sudah disediakan secara default pada platform trading.

Cara Pembacaan ADX dalam Analisa Teknikal

Hal pertama yang perlu kita pahami adalah kegunaan masing-masing garis. ADX menunjukkan kekuatan trend, sedangkan +DI dan -DI menunjukkan arah pergerakan harga.  Jika nilai ADX semakin tinggi, maka trend yang terjadi saat itu semakin kuat. Sebaliknya, jika ADX menunjukkan penurunan, maka trend yang terjadi semakin lemah.
+DI dan -DI menampilkan arah pergerakan harga. Ke
tika +DI berada diatas -Di artinya momentum yang terjadi pada market adalah sedang naik. Sebaliknya, ketika -DI berada diatas +DI artinya momentum yang terjadi pada market adalah sedang turun. Maka selanjutnya kita dapat mengkombinasikan semua itu dengan ADX sebagai penentu kekuatan trend akan sampai pada harga berapa.

